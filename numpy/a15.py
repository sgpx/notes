##Topic: NumPy Aggregation Functions##

##Challenge:## Create a function that takes a 3D NumPy array and computes the sum and mean for each 2D slice along a specified axis, returning a new array with these results. For instance, if given a shape of (3, 4, 5), aggregate along axis 0 to produce an array of shape (4, 5) with sums of each 2D slice.
